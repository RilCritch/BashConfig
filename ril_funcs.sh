## - utility for push fncs ---------------------------------------#
#### - Send it a location
putil()
{
	git add . && git commit -m "update" && git push --all
}

## - check status of github repos --------------------------------#
gitStatusAll()
{
	echo "| - Bash Configs - |"
	gb && git status
	echo "| - Notes - |"
	gn && git status
	echo "| - Misc Configs - |"
	gm && git status
}


